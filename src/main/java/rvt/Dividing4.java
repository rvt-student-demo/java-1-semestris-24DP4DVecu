package Dividing;

public class Dividing4 {
    public static void main(String[] args) {
        sum(3, 5);

        int number1 = 2;
        int number2 = 4;

        sum(number1, number2);

    }

    public static void sum(int first, int second) {
        System.out.println("The sum of numbers " + first + " and " + second + " is " + (first + second));
        
    }
}
